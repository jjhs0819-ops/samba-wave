def is_src(src, kws):
    sl = src.lower()
    return any(kw in sl for kw in kws)


def filter_to_leaves(cats):
    parents = set()
    for c in cats:
        parts = c.split(" > ")
        for i in range(1, len(parts)):
            parents.add(" > ".join(parts[:i]))
    return [c for c in cats if c not in parents]


def block(path, src):
    for kw in BLOCK:
        if kw in path:
            return True
    if not is_src(src, KIDS_S):
        for kw in KIDS_KW:
            if kw in path:
                return True
    if not is_src(src, PET_S):
        for kw in PET_KW:
            if kw in path:
                return True
    if not is_src(src, MATERN_S):
        for kw in MATERN_KW:
            if kw in path:
                return True
    return False


def get_keywords(src_cat):
    """source 마지막 세그먼트 + suffix 분해 + SYN 확장."""
    parts = [p.strip() for p in src_cat.split(">") if p.strip()]
    last = parts[-1] if parts else ""
    last_clean = re.sub(r"[^가-힣a-zA-Z0-9]", "", last)  # 특수문자 제거
    keys = set()
    if last:
        keys.add(last)
    # 분해
    for w in re.split(r"[/\s]+", last):
        if len(w) >= 2:
            keys.add(w)
    # SUFFIX_SYN 적용 — 마지막 세그먼트나 분해 단어가 SUFFIX_SYN의 키이면 동의어 추가
    expand = set(keys)
    for k in list(keys):
        if k in SUFFIX_SYN:
            expand.update(SUFFIX_SYN[k])
        # 끝글자 매칭: keys의 어떤 단어 끝이 SUFFIX_SYN 키와 일치
        for sk, sv in SUFFIX_SYN.items():
            if k.endswith(sk) and k != sk:
                expand.update(sv)
    return expand, last


def find_cands(src_cat, leaves, src_full, n=8):
    keys, last = get_keywords(src_cat)
    scored = []
    for c in leaves:
        if block(c, src_full):
            continue
        leaf_last = c.split(" > ")[-1] if " > " in c else c
        score = 0
        if last == leaf_last:
            score += 200
        for k in keys:
            if not k:
                continue
            if k == leaf_last:
                score += 150
            if k in leaf_last:
                score += 50
            if leaf_last.endswith(k):
                score += 80
            if k in c:
                score += 5
        if score > 0:
            scored.append((score, len(c), c))
    scored.sort(key=lambda x: (-x[0], x[1]))
    return [c for _, _, c in scored[:n]]


