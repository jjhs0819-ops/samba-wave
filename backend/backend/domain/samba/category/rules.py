def _kw_in_cat(kw: str, cat: str) -> bool:
    """카테고리 경로에서 키워드를 세그먼트 단위로 매칭.
    '한복신발'처럼 복합어 내 부분 문자열 오매핑을 방지한다.
    """
    for seg in cat.split(">"):
        seg = seg.strip()
        if kw == seg:
            return True
        if kw in seg.split():
            return True
        if len(kw) >= 3 and kw in seg:
            return True
    return False


def _filter_overseas(categories: list[str]) -> list[str]:
    """해외 관련 키워드가 포함된 카테고리를 제거한다."""
    return [c for c in categories if not any(kw in c for kw in _OVERSEAS_KEYWORDS)]


def _filter_to_leaves(categories: list[str]) -> list[str]:
    """리프 카테고리만 필터링 (하위 카테고리가 없는 경로만 반환)."""
    # 모든 부모 경로를 미리 추출하여 O(n) 조회
    parent_set: set[str] = set()
    for c in categories:
        parts = c.split(" > ")
        for i in range(1, len(parts)):
            parent_set.add(" > ".join(parts[:i]))
    return [c for c in categories if c not in parent_set]


