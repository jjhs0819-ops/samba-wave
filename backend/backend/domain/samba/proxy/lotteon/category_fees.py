def get_fee_rate_for_category(category_path: str | None) -> float:
    """카테고리 경로(예: '스포츠의류/운동화 > 남성스포츠의류 > 슬랙스')에서
    1뎁스(첫 토큰)만 잘라 수수료율을 반환.

    - 경로 공백 trim만 적용
    - 미매칭 시 DEFAULT_LOTTEON_FEE_RATE 폴백
    """
    if not category_path:
        return DEFAULT_LOTTEON_FEE_RATE
    first = category_path.split(">")[0].strip()
    return LOTTEON_CATEGORY_FEE_RATES.get(first, DEFAULT_LOTTEON_FEE_RATE)
